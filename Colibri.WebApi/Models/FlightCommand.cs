namespace Colibri.WebApi.Models;

/// <summary>
/// Класс, представляющий команду управления полетом БПЛА
/// Используется для отправки инструкций автопилоту или системе управления
/// </summary>
public class FlightCommand
{
    /// <summary>
    /// Флаг, указывающий необходимость выполнения взлета
    /// true - выполнить взлет на указанную высоту
    /// false - приземление
    /// </summary>
    public bool ShouldTakeoff { get; set; }
    /// <summary>
    /// Целевая высота полета в метрах
    /// Для взлета: высота, на которую необходимо подняться
    /// Для других команд: целевая высота для изменения
    /// </summary>
    public float AltitudeMeters { get; set; }
    /// <summary>
    /// Уникальный идентификатор команды для отслеживания и логирования
    /// Позволяет сопоставлять команды с их выполнением и результатами
    /// </summary>
    public string CommandId { get; set; }
    /// <summary>
    /// Флаг валидности команды
    /// true - команда прошла проверки и может быть выполнена
    /// false - команда содержит ошибки и должна быть отклонена
    /// </summary>
    public bool Valid { get; set; }
}
